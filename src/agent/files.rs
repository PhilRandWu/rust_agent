use crate::agent::plan::PlanOutput;
use serde::{Deserialize, Serialize};
use std::collections::BTreeMap;

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct FilesInput {
    pub plan: PlanOutput,
}

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct FilesOutput {
    pub files: BTreeMap<String, String>,
}

pub struct FilesNode;

impl FilesInput {
    pub fn new(plan: PlanOutput) -> FilesInput {
        FilesInput { plan }
    }
}

impl FilesNode {
    pub fn new() -> Self {
        Self
    }

    pub async fn run(&self, input: FilesInput) -> anyhow::Result<FilesOutput> {
        Ok(generate_files(input.plan))
    }
}

fn generate_files(plan: PlanOutput) -> FilesOutput {
    let mut files = BTreeMap::new();

    files.insert("package.json".to_string(), package_json(&plan.project_name));

    files.insert(
        "src/App.tsx".to_string(),
        app_tsx(&plan.project_name, &plan.description),
    );

    files.insert("src/main.tsx".to_string(), main_tsx());

    files.insert("src/styles.css".to_string(), styles_css());

    FilesOutput { files }
}

fn package_json(project_name: &str) -> String {
    const PKG_TEMPLATE: &str = r#"{{
  "scripts": {{
    "dev": "vite",
    "build": "vite build",
    "preview": "vite preview"
  }},
  "dependencies": {{
    "@vitejs/plugin-react": "latest",
    "vite": "latest",
    "react": "latest",
    "react-dom": "latest",
    "typescript": "latest"
  }},
  "devDependencies": {{}},
  "name": "{}",
  "version": "1.0.0"
}}"#;
    let pkg_name = sanitize_package_name(project_name);
    format!("{} {}", PKG_TEMPLATE, pkg_name)
}

fn sanitize_package_name(project_name: &str) -> String {
    let sanitized = project_name
        .trim()
        .to_lowercase()
        .chars()
        .map(|ch| {
            if ch.is_ascii_alphanumeric() || ch == '-' {
                ch
            } else if ch.is_whitespace() || ch == '_' {
                '-'
            } else {
                '-'
            }
        })
        .collect::<String>();

    let sanitized = sanitized.trim_matches('-').to_string();

    if sanitized.is_empty() {
        "rust-agent-app".to_string()
    } else {
        sanitized
    }
}

fn app_tsx(project_name: &str, description: &str) -> String {
    format!(
        r#"import React from "react";
import "./styles.css";

export default function App() {{
  return (
    <main className="app">
      <section className="hero">
        <p className="eyebrow">Generated by rust_agent</p>
        <h1>{}</h1>
        <p>{}</p>
      </section>
    </main>
  );
}}
"#,
        escape_jsx_text(project_name),
        escape_jsx_text(description)
    )
}

fn escape_jsx_text(text: &str) -> String {
    text.replace('&', "&amp;")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
        .replace('"', "&quot;")
        .replace('\'', "&#39;")
}

fn main_tsx() -> String {
    r#"import React from "react";
import ReactDOM from "react-dom/client";
import App from "./App";

ReactDOM.createRoot(document.getElementById("root")!).render(
  <React.StrictMode>
    <App />
  </React.StrictMode>
);
"#
    .to_string()
}

