#[cfg(test)]
mod tests {
    use crate::agent::files::{FilesInput, FilesNode, escape_jsx_text, sanitize_package_name};
    use crate::agent::plan::{ComponentPlan, PagePlan, PlanOutput};

    #[tokio::test]
    async fn generates_minimum_sandpack_files() {
        let node = FilesNode::new();

        let output = node
            .run(FilesInput::new(PlanOutput {
                project_name: "Todo App".to_string(),
                description: "A todo management application".to_string(),
                pages: vec![PagePlan {
                    name: "Home".to_string(),
                    route: "/".to_string(),
                    purpose: "Display todos".to_string(),
                }],
                components: vec![ComponentPlan {
                    name: "TodoList".to_string(),
                    purpose: "Render todos".to_string(),
                }],
            }))
            .await
            .expect("files node should generate files");

        assert!(output.files.contains_key("package.json"));
        assert!(output.files.contains_key("src/App.tsx"));
        assert!(output.files.contains_key("src/main.tsx"));
        assert!(output.files.contains_key("src/styles.css"));
        assert!(
            output
                .files
                .get("src/App.tsx")
                .unwrap()
                .contains("Todo App")
        );
    }

    #[test]
    fn sanitizes_package_name() {
        assert_eq!(sanitize_package_name("Todo App"), "todo-app");
        assert_eq!(sanitize_package_name("  My_App  "), "my-app");
        assert_eq!(sanitize_package_name("!!!"), "rust-agent-app");
    }

    #[test]
    fn escapes_jsx_text() {
        assert_eq!(
            escape_jsx_text(r#"<script>"x" & 'y'</script>"#),
            "&lt;script&gt;&quot;x&quot; &amp; &#39;y&#39;&lt;/script&gt;"
        );
    }
}
